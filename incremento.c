#include <stdio.h>


int main(){

        /*
        Incremento (++)
        Pré-incremento ++a
        Pós-incremento a++
        Decremento (--)
        Pré-decremento --a
        Pós-decremento a--
        */

    int numero1 = 1;

    printf("Antes do incremento: %d\n", numero1);
    numero1 ++;
    printf("Depois do incremento: %d\n", numero1);


    numero1 --;
    printf("Depois do decremento: %d\n", numero1);



    return 0;

}